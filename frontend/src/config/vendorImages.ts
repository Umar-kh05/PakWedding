/**
 * Get random vendor images for display
 * @param count Number of images to return
 * @returns Array of vendor image objects
 */
export function getRandomVendorImages(count: number = 5) {
  const shuffled = [...pakistaniVendorImages].sort(() => 0.5 - Math.random())
  return shuffled.slice(0, count)
}

/**
 * Get vendor images by category
 * @param category Category name
 * @returns Array of vendor image objects
 */
export function getVendorImagesByCategory(category: string) {
  return pakistaniVendorImages.filter(img => 
    img.category.toLowerCase() === category.toLowerCase()
  )
}

/**
 * Get all unique categories
 * @returns Array of category names
 */
export function getAllCategories() {
  return Array.from(new Set(pakistaniVendorImages.map(img => img.category)))
}
